# въвеждане размери на залата oт кнзлата в метри!
# изчисляваме размерите в см и  oт ширината изваждаме 100 см за коридора
# изчисляваме редвете ширина зала / ширина маса закръглено надолу
# изчисяваме колоните дължиан зала / дължина маса закръглено надолу
# общо маси = редове * колони - 3

import math
w = float(input())
h = float(input())

w_cm = w * 100
h_cm = h * 100 - 100

rows = math.floor(h_cm / 70)
coloums = math.floor(w_cm / 120)
tables = rows * coloums - 3
print(tables)

